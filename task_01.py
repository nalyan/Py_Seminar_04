# Вычислить число c заданной точностью d
# Пример:
# - при $d = 0.001, π = 3.141.$    $10^{-1} ≤ d ≤10^{-10}$

num = (input('задайте точность числа в формате 0.01: '))

if float(num) <= 0.1 and float(num) >= 10 ** (-10):
    d = int(len(num.split('.')[1]))
    pi = 3;
    for i in range(2, 3000, 4):
        pi = pi + (4 / (i * (i + 1) * (i + 2)) - (4 / ((i + 2) * (i + 3) * (i + 4))))
    print(round(pi, d))
else:
    print('задайте точность в диапазоне от 0.1 до 10^(-10)')
